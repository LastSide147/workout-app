import firestore from '@react-native-firebase/firestore';

// Firestore на React Native по умолчанию уже пишет данные на диск
// (офлайн-кэш включён "из коробки"), но размер кэша ограничен
// ~40 МБ. Когда лимит достигнут, SDK начинает вытеснять из кэша
// старые записи — и они перестают быть доступны офлайн. Для
// дневника тренировок, который копит записи месяцами, это может
// привести к тому, что старые дни "пропадут" без интернета.
// Снимаем лимит — кэш будет расти по мере необходимости.
//
// Важно: settings() нужно вызвать один раз и до первого обращения
// к любой коллекции Firestore, поэтому этот файл импортируется
// первой строкой в index.js.
firestore().settings({
  cacheSizeBytes: firestore.CACHE_SIZE_UNLIMITED,
});
